export const ALL_COMMITTEE_TEMPLATES: CommitteeTemplates[] = [
  { committeeType: 'Discipline', committeeName: 'لجنة الانضباط المدرسي', meetings: disciplineTemplates },
  { committeeType: 'Guidance', committeeName: 'لجنة التوجيه الطلابي', meetings: guidanceTemplates },
  { committeeType: 'Academic', committeeName: 'لجنة التحصيل الدراسي', meetings: academicTemplates },
];

export function getTemplatesForCommittee(committeeType: string): MeetingTemplate[] {
  return ALL_COMMITTEE_TEMPLATES.find(t => t.committeeType === committeeType)?.meetings ?? [];
}

export function getMeetingTemplate(committeeType: string, meetingNumber: number): MeetingTemplate | undefined {
  return getTemplatesForCommittee(committeeType).find(t => t.meetingNumber === meetingNumber);
}
